
#include <iostream>

using namespace std;

/*
 * Programa para ilustrar operaciones aritmeticas
 */
int main(int argc, char** argv) {

    // Tabulador o espaciador
    cout << "Tabulador:"<<endl;
    cout << "hola mundo"<<endl;
    cout << "hola\tmundo"<<endl;
    cout << "hola\t\tmundo"<<endl;
    
    // Nueva linea
    cout << "New Line:"<<endl;
    cout << "Hola\nmundo"<<endl;
    cout << "hola\n\nmundo"<<endl;
    cout << "hola\t\nmundo"<<endl;
    cout << "hola\n\tmundo"<<endl;
    
    return 0;
}

