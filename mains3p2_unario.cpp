#include <iostream>     // std::cout, std::hex, std::endl

int main () {
  
    int i = 0;
    std::cout << "i : " << i << std::endl;
    std::cout << "i++ : " << i++ << std::endl;
    std::cout << "++i : " << ++i << std::endl;
           
    int j = 7;
    std::cout << "j : " << j << std::endl;
    std::cout << "j-- : " << j-- << std::endl;
    std::cout << "--j : " << --j << std::endl;
  return 0;
}